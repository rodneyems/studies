package basico;

public class Variaveis {
	public static void main(String [] args) {
		int numeroInteiro = 10;
		double numeroQuebrado = 70.5;
		System.out.println(numeroInteiro + numeroQuebrado);
		
		double testeQuebrado = 50;		
		System.out.println(testeQuebrado);
		
		//casting
		int convertendoQuebrado = (int) numeroQuebrado;
		System.out.println(convertendoQuebrado);
	}
}
